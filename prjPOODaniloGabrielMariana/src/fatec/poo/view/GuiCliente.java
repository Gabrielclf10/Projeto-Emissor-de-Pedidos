    private void btnAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAlterarActionPerformed
        double limiteCredAnterior = ((Cliente)clientes.get(indiceCliente)).getLimiteCred();
        double limiteCredAtual = Double.valueOf(txtLimiteCred.getText());
        double gastoCliente = limiteCredAnterior - ((Cliente)clientes.get(indiceCliente)).getLimiteDisp();
        
        clientes.get(indiceCliente).setNome(txtNome.getText());
        clientes.get(indiceCliente).setEndereco((txtEndereco.getText()));    
        clientes.get(indiceCliente).setCidade((txtCidade.getText()));
        clientes.get(indiceCliente).setUf(String.valueOf(cbxUf.getSelectedItem()));
        clientes.get(indiceCliente).setDdd((txtDDD.getText()));
        clientes.get(indiceCliente).setTelefone((txtTelefone.getText()));
        clientes.get(indiceCliente).setCep((txtCep.getText()));
        ((Cliente)clientes.get(indiceCliente)).setLimiteCred(limiteCredAtual);
        ((Cliente)clientes.get(indiceCliente)).setLimiteDisp(limiteCredAtual - gastoCliente);
        resetarFormulario();
    }//GEN-LAST:event_btnAlterarActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        clientes.remove(indiceCliente);
        resetarFormulario();
    }//GEN-LAST:event_btnExcluirActionPerformed

