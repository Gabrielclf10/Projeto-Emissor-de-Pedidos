    private void habilitarCampos () {
        txtNome.setEnabled(true);
        txtEndereco.setEnabled(true);
        txtCidade.setEnabled(true);
        cbxUf.setEnabled(true);
        txtDDD.setEnabled(true);
        txtTelefone.setEnabled(true);
        txtCep.setEnabled(true);
        txtLimiteCred.setEnabled(true);
    }
    
    private void desabilitarCampos () {
        txtNome.setEnabled(false);
        txtEndereco.setEnabled(false);
        txtCidade.setEnabled(false);
        cbxUf.setEnabled(false);
        txtDDD.setEnabled(false);
        txtTelefone.setEnabled(false);
        txtCep.setEnabled(false);
        txtLimiteCred.setEnabled(false);
    }
    
    private void limparFormulario() {
        txtNome.setText(null);
        txtEndereco.setText(null);
        txtCidade.setText(null);
        cbxUf.setSelectedIndex(0);
        txtCep.setText(null);
        txtDDD.setText(null);
        txtTelefone.setText(null);
        txtLimiteCred.setText(null);
        lblLimiteDisp.setText(null);
    }
    
    private void resetarFormulario() {
        limparFormulario();
        desabilitarCampos();
        btnIncluir.setEnabled(false);
        btnAlterar.setEnabled(false);
        btnExcluir.setEnabled(false);
    }
    
    private void btnConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConsultarActionPerformed
        indiceCliente = -1;
        if (clientes.size() > 0) {
            for (int i = 0; i < clientes.size(); i++) {
                if (txtCpf.getText().equals(clientes.get(i).getCpf()) && clientes.get(i) instanceof Cliente) {
                    indiceCliente = i;                   
                    break; 
                }
            }   
        }
    
        if (indiceCliente >= 0){
            txtNome.setText(clientes.get(indiceCliente).getNome());
            txtEndereco.setText(clientes.get(indiceCliente).getEndereco());
            txtCidade.setText(clientes.get(indiceCliente).getCidade());
            cbxUf.setSelectedItem(clientes.get(indiceCliente).getUf());
            txtDDD.setText(clientes.get(indiceCliente).getDdd());
            txtTelefone.setText(clientes.get(indiceCliente).getTelefone());
            txtCep.setText(clientes.get(indiceCliente).getCep());
            txtLimiteCred.setText(String.valueOf(((Cliente)clientes.get(indiceCliente)).getLimiteCred()));
            lblLimiteDisp.setText(String.valueOf(((Cliente)clientes.get(indiceCliente)).getLimiteDisp()));
            
            btnIncluir.setEnabled(false);
            btnAlterar.setEnabled(true);
            btnExcluir.setEnabled(true);
            
        } else {
            limparFormulario();
            btnIncluir.setEnabled(true);
            btnAlterar.setEnabled(false);
            btnExcluir.setEnabled(false);
        }
        
        habilitarCampos();
    }//GEN-LAST:event_btnConsultarActionPerformed

    private void btnIncluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIncluirActionPerformed
        objCliente = new Cliente(txtCpf.getText(), txtNome.getText(), Double.valueOf(txtLimiteCred.getText()));       
        objCliente.setEndereco(txtEndereco.getText());
        objCliente.setCidade(txtCidade.getText());
        objCliente.setUf(String.valueOf(cbxUf.getSelectedItem()));
        objCliente.setDdd(txtDDD.getText());
        objCliente.setTelefone(txtTelefone.getText());
        objCliente.setCep(txtCep.getText());
        clientes.add(objCliente);
        resetarFormulario();
    }//GEN-LAST:event_btnIncluirActionPerformed

