    private void calcQuantidadeAndTotalDoPedido() {
        double valTotalPedido = 0;
        double quantidadeItensPedido = 0;
        if (pedido.getItensPedido().size() > 0) {
            for (int i = 0; i < pedido.getItensPedido().size(); i++) {
                ItemPedido itemPedido = pedido.getItensPedido().get(i);
                valTotalPedido += itemPedido.getQtdeVendida() * itemPedido.getProduto().getPreco();
                quantidadeItensPedido += itemPedido.getQtdeVendida();
            }
        }
        lblValorTotalPedido.setText(df.format(valTotalPedido));
        lblQtdeItensPedidos.setText(df.format(quantidadeItensPedido));
    }
    
    private void btnConsultarPedidoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConsultarPedidoActionPerformed
        indicePedido = -1;
        if (cadPedido.size() > 0) {
            for (int i = 0; i < cadPedido.size(); i++) {
                if (txtNumeroPedido.getText().equals(cadPedido.get(i).getNumero())) {
                    indicePedido = i;                   
                    break; 
                }
            }   
        }
        
        if (indicePedido >= 0) {
            pedido = cadPedido.get(indicePedido);
            txtCodigoProduto.setEnabled(true);
            btnConsultarProduto.setEnabled(true);
            btnAlterar.setEnabled(true);
            btnExcluir.setEnabled(true);
            btnRemoverItem.setEnabled(true);
            
            txtNumeroPedido.setText(pedido.getNumero());
            txtDataPedido.setText(pedido.getDataEmissao());
            cbxFormaPagamento.setSelectedIndex(pedido.getFormaPagto());
            txtCpfCliente.setText(pedido.getCliente().getCpf());
            lblNomeCliente.setText(pedido.getCliente().getNome());
            txtCpfVendedor.setText(pedido.getVendedor().getCpf());
            lblNomeVendedor.setText(pedido.getVendedor().getNome());
            
            for (int i = 0; i < pedido.getItensPedido().size(); i++) {
                ItemPedido itemPedido = pedido.getItensPedido().get(i);
                double subTotal = itemPedido.getQtdeVendida() * itemPedido.getProduto().getPreco();
                String linha[] = {
                    itemPedido.getProduto().getCodigo(),
                    itemPedido.getProduto().getDescricao(),
                    df.format((itemPedido.getProduto().getPreco())),
                    df.format(itemPedido.getQtdeVendida()),
                    df.format(subTotal)
                };
                modTableProdutos.addRow(linha);
                itensPedidoSalvo.add(itemPedido);
            }
            
            calcQuantidadeAndTotalDoPedido();
        } else {
            txtDataPedido.setEnabled(true);
            cbxFormaPagamento.setEnabled(true);
            btnConsultarCliente.setEnabled(true);
            btnIncluir.setEnabled(true);
        }
        
        btnConsultarPedido.setEnabled(false);
    }//GEN-LAST:event_btnConsultarPedidoActionPerformed

    private void btnConsultarClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConsultarClienteActionPerformed
        indiceCliente = -1;
        if (cadCliVend.size() > 0) {
            for (int i = 0; i < cadCliVend.size(); i++) {
                if (txtCpfCliente.getText().equals(cadCliVend.get(i).getCpf()) && cadCliVend.get(i) instanceof Cliente) {
                    indiceCliente = i;                   
                    break; 
                }
            }   
        }
        
        if (indiceCliente >= 0) {
            pedido.setCliente((Cliente)cadCliVend.get(indiceCliente));
            lblNomeCliente.setText(cadCliVend.get(indiceCliente).getNome());
            txtCpfCliente.setEnabled(false);
            btnConsultarCliente.setEnabled(false);
            txtCpfVendedor.setEnabled(true);
            btnConsultarVendedor.setEnabled(true);
        } else {
            JOptionPane.showMessageDialog(null, 
                "Cliente informado não foi cadastrado.", 
                "Aviso", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnConsultarClienteActionPerformed

