    private void btnAdicionarItemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAdicionarItemActionPerformed
        double quantidade = 0;
        if (!txtQtdeVendida.getText().isEmpty()) {
            quantidade = Double.parseDouble(txtQtdeVendida.getText());
        }
        
        if (quantidade == 0) {
            JOptionPane.showMessageDialog(null, 
                "Não é possível adicionar o produto com a quantidade igual a 0 (ZERO).", 
                "Aviso", JOptionPane.WARNING_MESSAGE);
        } else if (quantidade > cadProd.get(indiceProd).getQtdeEstoque()) {
            JOptionPane.showMessageDialog(null, 
                "Não há estoque suficiente para adicionar a quantidade pedida pelo produto.", 
                "Aviso", JOptionPane.WARNING_MESSAGE);
        } else {
            int sequencia = pedido.getItensPedido().size();
            ItemPedido itemPedido = new ItemPedido(sequencia, quantidade, cadProd.get(indiceProd));
            double subTotal = quantidade * itemPedido.getProduto().getPreco();
            
            if (subTotal > pedido.getCliente().getLimiteDisp()) {
                JOptionPane.showMessageDialog(null, 
                "O cliente não há saldo suficiente para adicionar esse item.", 
                "Aviso", JOptionPane.WARNING_MESSAGE);
            } else {
                String linha[] = {
                    itemPedido.getProduto().getCodigo(),
                    itemPedido.getProduto().getDescricao(),
                    df.format((itemPedido.getProduto().getPreco())),
                    df.format(itemPedido.getQtdeVendida()),
                    df.format(subTotal)
                };
                modTableProdutos.addRow(linha);
                pedido.addItemPedido(itemPedido);
                calcQuantidadeAndTotalDoPedido();
            }
        }
    }//GEN-LAST:event_btnAdicionarItemActionPerformed

    private void btnRemoverItemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoverItemActionPerformed
        if (tblProdutos.getSelectedRow() == -1) {
            JOptionPane.showMessageDialog(null, 
                "Selecione um produto para remover da lista.", 
                "Aviso", JOptionPane.WARNING_MESSAGE);
        } else {
            pedido.removerItemPedido(tblProdutos.getSelectedRow());
            modTableProdutos.removeRow(tblProdutos.getSelectedRow());
            calcQuantidadeAndTotalDoPedido();
        }
    }//GEN-LAST:event_btnRemoverItemActionPerformed

