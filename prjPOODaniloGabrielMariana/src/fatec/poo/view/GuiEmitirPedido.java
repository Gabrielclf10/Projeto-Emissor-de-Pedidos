    private void btnSairActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSairActionPerformed
        if (!itensPedidoSalvo.isEmpty()) {
            // Limpar a lista e recuperar dados de estoque e limite de crédito do cliente
            int indiceUltimoItemPedido = pedido.getItensPedido().size() - 1;
            for (int i = indiceUltimoItemPedido; i >= 0; i--) pedido.removerItemPedido(i);
            
            // Recuperar dados do pedido recalculando estoque e limite de crédito cliente
            for (int i = 0; i < itensPedidoSalvo.size(); i++) pedido.addItemPedido(itensPedidoSalvo.get(i));
        }
        dispose();
    }//GEN-LAST:event_btnSairActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        // Recuperar dados de estoque e limite de crédito do cliente
        int indiceUltimoItemPedido = pedido.getItensPedido().size() - 1;
        for (int i = indiceUltimoItemPedido; i >= 0; i--) pedido.removerItemPedido(i);
        cadPedido.remove(indicePedido);
        resetarFormulario();
    }//GEN-LAST:event_btnExcluirActionPerformed

